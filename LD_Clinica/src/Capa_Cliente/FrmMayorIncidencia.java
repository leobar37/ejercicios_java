   private void LlenarLista(){
       DefaultListModel modelo = new DefaultListModel();
       String incidencia = Paciente.incidencia();
       String  part[] = incidencia.split("%");
       if(part.length>1)
           for(int i = 0;i<part.length;i++){
               modelo.addElement(part[i]);
           }
       else
           modelo.addElement(incidencia);
       
        lista.setModel(modelo);
   }
